    private void loadSiswa() {
    try {
        String sql = "SELECT id_siswa, nama FROM siswa";
        Connection conn = Koneksi.getConnection();
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
            cbSiswa.addItem(rs.getString("id_siswa") + " - " + rs.getString("nama"));
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
}

    private void tampilAbsensi() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Nama");
    model.addColumn("Tanggal");
    model.addColumn("Status");

    try {
        String sql = "SELECT absensi.id_absen, siswa.nama, absensi.tanggal, absensi.status " +
                     "FROM absensi JOIN siswa ON absensi.id_siswa = siswa.id_siswa";
        Connection conn = Koneksi.getConnection();
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt(1),
                rs.getString(2),
                rs.getDate(3),
                rs.getString(4)
            });
        }
        tableAbsensi.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
}


