    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
        String siswa = cbSiswa.getSelectedItem().toString();
        String idSiswa = siswa.split(" - ")[0];

        String sql = "INSERT INTO absensi (id_siswa, tanggal, status) VALUES (?,?,?)";
        Connection conn = Koneksi.getConnection();
        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setInt(1, Integer.parseInt(idSiswa));
        pst.setDate(2, java.sql.Date.valueOf(txtTanggal.getText()));
        pst.setString(3, cbStatus.getSelectedItem().toString());

        pst.execute();
        JOptionPane.showMessageDialog(this, "Absensi tersimpan");
        tampilAbsensi();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

